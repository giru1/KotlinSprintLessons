package org.example.lesson_3

fun main() {
    val firstName = "Татьяна"
    val middleName = "Сергеевна"
    var lastName = "Андреева"
    var age = 20

    // Вывод данных в 20 лет
    println("[$lastName $firstName $middleName, $age]")

    lastName = "Сидорова"
    age = 22

    // Вывод данных в 22 года
    println("[$lastName $firstName $middleName, $age]")

}
